#define CASE_RETURN(x) case x: return L ## #x
LPCWSTR StringFromState(DWORD state) {
    switch (state) {
        CASE_RETURN(DEVICE_STATE_ACTIVE);
        CASE_RETURN(DEVICE_STATE_DISABLED);
        CASE_RETURN(DEVICE_STATE_NOTPRESENT);
        CASE_RETURN(DEVICE_STATE_UNPLUGGED);
        default: return L"Unrecognized";
    }
}

